package com.entity;

public class Student {
	private int id;
	private String name;
	private String pwd;
	private String sex;
	private int age;
	private double gpascores;
	private String pro;
	private int stuclass;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public double getGpascores() {
		return gpascores;
	}
	public void setGpascores(double gpascores) {
		this.gpascores = gpascores;
	}
	public String getPro() {
		return pro;
	}
	public void setPro(String pro) {
		this.pro = pro;
	}
	public int getStuclass() {
		return stuclass;
	}
	public void setStuclass(int stuclass) {
		this.stuclass = stuclass;
	}
	
}
